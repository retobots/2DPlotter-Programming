/*======================================================== [INCLUDE LIBRARY] ===================================================*/

#include "ServoHAL.hpp"

/*=====================================================  [FUNCTION DEFINITIONS] ================================================*/

/*========================================================== ( Hàm Setup ) =====================================================*/

void setupServo()
{
    myServo.setPeriodHertz(50);
    myServo.attach(PWM_PIN, 500, 2400);
    penUp();
}

/*========================================================= ( Hàm Nâng Bút ) ===================================================*/

void penUp()
{
    penServo.write(PEN_UP);
    delay(300);
}

/*========================================================== ( Hàm Hạ Bút ) ====================================================*/

void penDown()
{
    penServo.write(PEN_DOWN);
    delay(300);
}